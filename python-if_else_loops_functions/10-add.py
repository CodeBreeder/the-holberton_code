#!/usr/bin/python3

# Function that returns the sum of two integers
def add(a, b):
    """
    Add two numbers and return the result.

    Parameters:
    a (int or float): The first number.
    b (int or float): The second number.

    Returns:
    int or float: The sum of the two input numbers.
    """
    result = int(a) + int(b)
    return result
