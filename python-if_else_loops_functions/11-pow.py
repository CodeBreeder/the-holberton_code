
# Function that computes and returns a to the power of b
def power(a, b):
    """
    Calculate the result of raising a number to a given exponent.

    Parameters:
    base (int or float): The base number.
    exponent (int or float): The exponent to which the base is raised.

    Returns:
    int or float: The result of base raised to the power of the exponent.
    """
    result = a ** b
    return result
